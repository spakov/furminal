namespace Terminal {
  /// <summary>
  /// Supported cursor styles.
  /// </summary>
  public enum CursorStyles {
    /// <summary>
    /// A block cursor, like █.
    /// </summary>
    Block = 1,

    /// <summary>
    /// An underline cursor, like _.
    /// </summary>
    Underline = 2,

    /// <summary>
    /// A bar cursor, like |.
    /// </summary>
    Bar = 3
  }
}
